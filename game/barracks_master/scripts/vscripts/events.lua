-- The overall game state has changed
function GameMode:OnGameRulesStateChange(keys)
  DebugPrint("[BAREBONES] GameRules State Changed")
  DebugPrintTable(keys)

  -- This internal handling is used to set up main barebones functions
  GameMode:_OnGameRulesStateChange(keys)
  
  -------------------------
  -- Custom Creep Spawns --
  -------------------------
  local newState = GameRules:State_Get()
  if newState == DOTA_GAMERULES_STATE_CUSTOM_GAME_SETUP then
    BMConvars:Setup()
    LastHits:Setup()
    ScoreboardUpdater:Setup()
  elseif newState == DOTA_GAMERULES_STATE_HERO_SELECTION then
    self:PostLoadPrecache()
  elseif newState == DOTA_GAMERULES_STATE_GAME_IN_PROGRESS then
    SpawnSynchronizer:Setup()
    AlchemistGifter:Setup()

    -- Show cheat message if not on tools
    if not Convars:GetBool("developer") and GameRules:IsCheatMode() then
        GameRules:SendCustomMessage("#bm_cheats_enabled", 0, 0)
    end

  -- Post Game Stats
  elseif newState >= DOTA_GAMERULES_STATE_POST_GAME then
    Timers:CreateTimer(0.03, function()
      if statCollection and statCollection.sendBMPost and not statCollection.sent_BMPost then
        statCollection.sent_BMPost = true
        statCollection:sendBMPost()
      end
    end)
  end
end

-- An NPC has spawned somewhere in game.  This includes heroes
function GameMode:OnNPCSpawned(keys)
  DebugPrint("[BAREBONES] NPC Spawned")
  DebugPrintTable(keys)

  -- This internal handling is used to set up main barebones functions
  GameMode:_OnNPCSpawned(keys)

  local npc = EntIndexToHScript(keys.entindex)
  -- All new spawns start with phased
  if npc then
    npc:AddNewModifier( npc, nil, "modifier_phased", {duration=3.0} )
  end

  if npc and npc:IsRealHero() then
    -- Heroes gain 5 seconds of invisibility when spawning
    local dur = 5.0
    npc:AddNewModifier(npc, nil, "modifier_invisible", {duration=dur})
  end

end

